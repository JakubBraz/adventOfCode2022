# 1 rock
# 2 paper
# 3 scissors

rules = {1: 3, 2: 1, 3: 2}
losing = {3: 1, 1: 2, 2: 3}

decode_him = {"A": 1, "B": 2, "C": 3}
decode_me = {"X": 1, "Y": 2, "Z": 3}
decode_me2 = {"X": 0, "Y": 3, "Z": 6}


def parse_inp(i):
    result = i.split('\n')
    return result


def play_round(him, me):
    him = decode_him[him]
    me = decode_me[me]

    result = 3
    if me != him:
        if rules[me] == him:
            result += 3
        else:
            result -= 3
    return result + me


def play_round2(him, me):
    him = decode_him[him]
    outcome = decode_me2[me]

    result = 0
    if outcome == 6:
        result += losing[him]
    elif outcome == 3:
        result += him
    else:
        result += rules[him]

    return result + outcome


def main():
    i = parse_inp(day_input())
    result1 = sum([play_round(r[0], r[2]) for r in i])
    print(result1)
    result2 = sum([play_round2(r[0], r[2]) for r in i])
    print(result2)


